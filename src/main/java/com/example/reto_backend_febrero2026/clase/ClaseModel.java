	ClaseModel(int famiCod, int subfCod, int cod, String description) {
		this.famiCod = famiCod;
		this.subfCod = subfCod;
		this.cod = cod;
		this.description = description;
	}

	@JsonProperty("fami_cod")
	public int getFamiCod() {
		return famiCod;
	}

	@JsonProperty("subf_cod")
	public int getSubfCod() {
		return subfCod;
	}

	public int getCod() {
		return cod;
	}

	public String getDescription() {
		return description;
	}

	public static ClaseModel findByFamiSubfCod(int famiCod, int subfCod, int cod) {
		for (ClaseModel clase : values()) {
			if (clase.famiCod == famiCod && clase.subfCod == subfCod && clase.cod == cod) {
				return clase;
			}
		}
		throw new IllegalArgumentException("No Clase found with famiCod: " + famiCod + ", subfCod: " + subfCod + " and cod: " + cod);
	}

	public static ClaseModel[] findByFamiCodAndSubfCod(int famiCod, int subfCod) {
		return Arrays.stream(values())
				.filter(clase -> clase.famiCod == famiCod && clase.subfCod == subfCod)
				.toArray(ClaseModel[]::new);
	}

	public static ClaseModel[] findByFamiCod(int famiCod) {
		return Arrays.stream(values())
				.filter(clase -> clase.famiCod == famiCod)
				.toArray(ClaseModel[]::new);
	}
